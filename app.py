@app.route("/submit", methods=["POST"])
@limiter.limit("10 per minute;100 per day")
def submit():
    data = request.get_json(force=True)
    text = data.get("text", "").strip()
    creator_id = data.get("creator_id", "anonymous")

    if not text:
        return jsonify({"error": "text field is required"}), 400
    if len(text) < 10:
        return jsonify({"error": "text is too short for analysis"}), 400

    content_id = str(uuid.uuid4())

    # Run both signals
    llm_score, llm_reasoning = llm_classify(text)
    style_score = stylometric_score(text)

    # Combine into confidence
    confidence = compute_confidence(llm_score, style_score)
    attribution = attribution_label(confidence)
    label = transparency_label(attribution, confidence)

    # Write to audit log
    log_submission(content_id, creator_id, attribution, confidence, llm_score, style_score, label)

    return jsonify({
        "content_id": content_id,
        "attribution": attribution,
        "confidence": confidence,
        "confidence_pct": round(confidence * 100),
        "llm_score": llm_score,
        "style_score": style_score,
        "llm_reasoning": llm_reasoning,
        "label": label,
        "status": "classified"
    })


@app.route("/appeal", methods=["POST"])
def appeal():
    data = request.get_json(force=True)
    content_id = data.get("content_id", "").strip()
    reasoning = data.get("creator_reasoning", "").strip()

    if not content_id:
        return jsonify({"error": "content_id is required"}), 400
    if not reasoning:
        return jsonify({"error": "creator_reasoning is required"}), 400

    entry = get_entry(content_id)
    if not entry:
        return jsonify({"error": "content_id not found"}), 404

    if entry.get("status") == "under_review":
        return jsonify({"error": "an appeal is already pending for this content"}), 409

    success = log_appeal(content_id, reasoning)
    if not success:
        return jsonify({"error": "failed to record appeal"}), 500

    return jsonify({
        "content_id": content_id,
        "status": "appeal_received",
        "message": "Your appeal has been recorded and the content is now under review. A human reviewer will assess your submission.",
        "original_attribution": entry.get("attribution"),
        "original_confidence": entry.get("confidence"),
    })


@app.route("/log", methods=["GET"])
def view_log():
    limit = request.args.get("limit", 50, type=int)
    entries = get_log(limit)
    return jsonify({"count": len(entries), "entries": entries})


@app.route("/status/<content_id>", methods=["GET"])
def status(content_id):
    entry = get_entry(content_id)
    if not entry:
        return jsonify({"error": "content_id not found"}), 404
    return jsonify(entry)


@app.route("/health", methods=["GET"])
def health():
    return jsonify({"status": "ok"})


